left join (select b.uid,b.schedule_week,b.wvdate,max(b._submission_time) _submission_time
from 
(
select vf1.uid,s.jivita_week as schedule_week,vf1.wvdate,_submission_time 
from [mindr-live].dbo.all_wra_vf vf1
left join [mindr-live].dbo.schedule_wra s on s.id = vf1.schedule_id
left join (select vf2.uid,max(s.jivita_week) as schedule_week,max(wvdate) wvdate 
from [mindr-live].dbo.all_wra_vf vf2
left join [mindr-live].dbo.schedule_wra s on s.id = vf2.schedule_id
where vf2.duplicate is null and wvvisit=1
group by vf2.uid
) a on a.uid = vf1.uid and vf1.wvdate =a.wvdate and s.jivita_week =a.schedule_week  
where a.wvdate is not null
and wvvisit=1
and vf1.duplicate is null 
) b group by b.uid,b.schedule_week,b.wvdate) c on c.uid = vf.uid and c.schedule_week = s.jivita_week
and c.wvdate = vf.wvdate and c._submission_time = vf._submission_time
